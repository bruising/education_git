package com.pengyipeng.education.model.entity;


public class CourseProject {

  private Integer projectCourseId;
  private Integer projectId;
  private Integer courseId;


  public Integer getProjectCourseId() {
    return projectCourseId;
  }

  public void setProjectCourseId(Integer projectCourseId) {
    this.projectCourseId = projectCourseId;
  }


  public Integer getProjectId() {
    return projectId;
  }

  public void setProjectId(Integer projectId) {
    this.projectId = projectId;
  }


  public Integer getCourseId() {
    return courseId;
  }

  public void setCourseId(Integer courseId) {
    this.courseId = courseId;
  }

}
